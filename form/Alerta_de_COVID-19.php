  <div class="card-header"><h2> O que é o Corona Virus / COVID19</h2></div>
  <div class="card-body text-dark">
    <h5 class="card-title">Os coronavírus são uma grande família de vírus que podem causar doenças em animais ou humanos. Em humanos, sabe-se que vários coronavírus causam infecções respiratórias que variam do resfriado comum a doenças mais graves. O coronavírus descoberto mais recentemente causa a doença COVID-19.</h5>
        <h2>As luvas e a COVID-19.</h2>
    <h5><p class="card-text">Desde o início da pandemia de COVID-19, com o constante apelo para uma higiene frequente das mãos, vai-se tornando cada vez mais frequente encontrar pessoas de luvas na rua, em supermercados, a passear e até a conduzir viaturas automóveis. De acordo com as indicações da OMS e do Centro de Controlo de Doenças (CDC), o uso de luvas obedece a regras que incluem a sua utilização em situações específicas. Até agora não existem evidências científicas de transmissão do vírus através da pele intacta, não havendo, assim, indicação para a sua utilização em situações do quotidiano. No entanto, pessoas com feridas, ou qualquer situação clínica que comprometa a continuidade da pele (ex.: psoríase, eczema, etc.) devem usar luvas.</p></h5>
      <h2>Covid-19 afeta crianças.?</h2>
    <h5><p class="card-text">Esse é um vírus novo e ainda não sabemos o suficiente sobre como ele afeta crianças ou mulheres grávidas. Sabemos que é possível que pessoas de qualquer idade sejam infectadas pelo vírus, mas até agora houve relativamente poucos casos de Covid-19 entre crianças. O vírus é fatal em casos raros, até agora principalmente entre pessoas idosas com condições médicas preexistentes.</p></h5>
      <h2>Devo me importar com isso?.</h2>
    <h5><p class="card-text">A doença devido à infecção PELO NOVO CORONAVÍRUS é geralmente leve, especialmente em crianças e adultos jovens. No entanto, pode causar doenças graves: cerca de 1 em cada 5 pessoas precisam de cuidados hospitalares. Portanto, é bastante COMUM que as pessoas se preocupem com o impacto do surto de COVID-19, entre elas e seus entes queridos..</p></h5>
      <h2>Transmissão</h2>
    <h5><p class="card-text">A doença pode se espalhar entre pessoas, através de pequenas gotículas do nariz ou da boca que se espalham quando uma pessoa com COVID-19 tosse, espirra ou fala.Essas gotículas podem entrar em contato diretamente com o olho, nariz ou boca de outra pessoa ou se acumulam em objetos e superfícies. Quando alguem toca nesses objetos ou superfícies, podem se infectar ao tocar nos olhos, nariz ou boca. É por isso que é importante ficar a mais de 1 metro (3 pés) de uma pessoa doente..</p></h5>
